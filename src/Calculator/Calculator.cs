
namespace Calculator
{
    public class Calculator
    {
        public int AddInts(int a, int b)
        {
            return a + b;
        }

        public double AddDoubles(double a, double b)
        {
            return a + b;
        }

        public int Devide(int a, int b)
        {
            return a/b;
        }
    }
}
